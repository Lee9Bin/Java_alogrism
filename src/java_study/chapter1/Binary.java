package java_study.chapter1;

public class Binary {
    public static void main(String[] args){
        int num = 10;
//        2진수표현법 0B를 써주고 뒤에
        int bNum = 0B1010;
//        8진수표현법 012
        int oNum = 012;
//        16진수표현법 0X
        int xNum = 0XA;
        System.out.println(num);
        System.out.println(bNum);
        System.out.println(oNum);
        System.out.println(xNum);

    }
}
